public class Cofrinho {
    private String proprietario;
    private double saldo;

    public Cofrinho(String proprietario, double saldo) {
        this.proprietario = proprietario;
        this.saldo = saldo;
    }

    public String getProprietario() {
        return proprietario;
    }

    public double getSaldo() {
        return saldo;
    }
}

